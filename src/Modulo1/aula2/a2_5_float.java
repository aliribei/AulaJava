package Modulo1.aula2;

public class a2_5_float {

    public static void main(String args[]){

        float menor_valor = Float.MIN_VALUE;
        float maior_valor = Float.MAX_VALUE;

        System.out.print("Menor valor float: " + menor_valor + "\n");
        System.out.print("Maior valor float: " + maior_valor + "\n");

        float salario = 800.00f;
        System.out.print("Salario: " + salario +  "\n");

        float massa = 5.972E24f;
        System.out.print("Massa da terra: " + massa +  "\n");

    }
}
